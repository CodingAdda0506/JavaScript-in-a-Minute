// Comparison Operators
let num1 = 10;
let num2 = 5;

// Equality (loose equality, type coercion)
console.log(num1 == num2); // false

// Strict Equality (value and type equality)
console.log(num1 === num2); // false

// Inequality (loose inequality, type coercion)
console.log(num1 != num2); // true

// Strict Inequality (value or type inequality)
console.log(num1 !== num2); // true 

// Greater than
console.log(num1 > num2); // true

// Less than
console.log(num1 < num2); // false

// Greater than or equal to
console.log(num1 >= num2); // true

// Less than or equal to
console.log(num1 <= num2); // false