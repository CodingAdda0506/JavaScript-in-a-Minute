/*
Assignment Operators:

Assignment: =
Addition assignment: +=
Subtraction assignment: -=
Multiplication assignment: *=
Division assignment: /=
Remainder assignment (Modulus): %=
Exponentiation assignment: **=
*/

let a = 100;
let b = 5;

// Assignment
let assignment = a;
console.log(assignment);

// Addition assignment
assignment += b;

assignment = assignment + b;
console.log(assignment);

// Subtraction assignment
assignment -= b;
console.log(assignment);

// Multiplication assignment
assignment *= b;
console.log(assignment);

// Division assignment
assignment /= b;
console.log(assignment);

// Remainder assignment
assignment %= b;
console.log(assignment);

// Exponentiation assignment
assignment **= b;
console.log(assignment);
